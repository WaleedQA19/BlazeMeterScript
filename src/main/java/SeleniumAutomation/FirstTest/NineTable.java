package SeleniumAutomation.FirstTest;

public class NineTable {

	public static void main(String[] args) {
		
		        int number = 9;
		        int limit = 10;

		        for (int i = 1; i <= limit; i++) {
		            System.out.println(number + " * " + i + " = " + (number * i));
		        }
	

	}

}
